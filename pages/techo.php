                                     <h2>TECHO</h2>
                                    <p>TECHO es una organización presente en Latinoamérica y el Caribe, que busca superar la situación de <strong>pobreza</strong> que viven miles de personas en los <strong>asentamientos</strong> informales, a través de la <strong>acción conjunta de sus vecinos y voluntarios.</strong></p>
                                    <p>Con la implementación de un modelo de intervención enfocado en el <strong>desarrollo comunitario</strong>, TECHO busca, a través de la ejecución de diferentes planes, construir una sociedad <strong>justa y sin pobreza</strong>, donde todas las personas tengan la oportunidad desarrollar sus <strong>capacidades</strong>, ejercer y gozar plenamente de sus <strong>derechos.</strong></p>
                                    <p>Somos una organización no partidaria y sin orientación religiosa.</p>
