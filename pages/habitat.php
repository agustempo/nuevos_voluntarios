                                     <h2>Desarrollo de Hábitat</h2>
<p>El área de desarrollo de hábitat es una fase de TECHO que promueve la implementación de soluciones definitivas en asentamientos precarios.
El objetivo principal es transformar asentamiento precario en comunidades integradas social y territorialmente que les permita alcanzar su pleno desarrollo como familias y sujetos de derecho en esta sociedad.
La comprensión del hábitat adecuado nos lleva a identificar al menos seis temas específicos con los cuales podemos trabajar:</p>
<p>
1) Regularización de la propiedad para la seguridad jurídica de los terrenos<br>

2) Acceso a servicios básicos de manera formal y regular<br>

3) Acceso a infraestructura de ciudad, como calles pavimentadas, alumbrado público, cercanía de transporte público, colegios, y hospitales.<br>

4) Espacios habitables, con dimensiones y materiales adecuados, asegurando un alto estándar técnico en espacios colectivos adecuados.<br>

5) Asegurar que la solución en el tiempo sea inversión pública y no gasto social, identificando atributos que agreguen valor y plusvalía.<br>

6) Comunidades organizadas en base a sus necesidades colectivas, capaces de resolver sus problemas.<br>
<br>
Es importante destacar que el perfil de voluntarios que demandan las comunidades para este tipo de proyectos es un perfil técnico particularmente enfocado en temas legales y urbanos.</p>

